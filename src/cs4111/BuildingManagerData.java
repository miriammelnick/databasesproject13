package cs4111;

public class BuildingManagerData {
	String numberFloors, area, location, managerSince;
	int buildingId, managerId;
	String choice;
	
	public String getNumberFloors() {
		return numberFloors;
	}
	public void setNumberFloors(String numberFloors) {
		this.numberFloors = numberFloors;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getManagerSince() {
		return managerSince;
	}
	public void setManagerSince(String managerSince) {
		this.managerSince = managerSince;
	}
	public int getBuildingId() {
		return buildingId;
	}
	public void setBuildingId(int buildingId) {
		this.buildingId = buildingId;
	}
	public int getManagerId() {
		return managerId;
	}
	public void setManagerId(int managerId) {
		this.managerId = managerId;
	}
	public String getChoice() {
		return choice;
	}
	public void setChoice(String choice) {
		this.choice = choice;
	}
	
}
